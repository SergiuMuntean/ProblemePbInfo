#include <stdio.h>
#include <stdlib.h>
#include <math.h>

int main()
{
    float x = 2.15;
    printf("The ceil of x is %.0f\t", ceil(x));
    printf("The floor of x is %.0f\t", floor(x));
    printf("The value of e^2 is %f\t", exp(1));
    printf("The natural logarithm of 5 is %f\n", log(2.718281));
    printf("The decimal logarithm of 100 is %f", log10(100));
    return 0;
}
