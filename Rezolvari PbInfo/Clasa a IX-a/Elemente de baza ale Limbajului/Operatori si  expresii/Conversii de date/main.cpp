#include <iostream>
using namespace std;

int main()
{
    int n;
    double x = 5.75;

    /// conversie de la double la int

    n = x;

    cout << "n = " << n << endl;
    cout << "x = " << x << endl;
    char c='A';
    cout << (int) c << endl; // 65
    cout << char(97) << endl; // a
    return 0;
}
