#include <iostream>

using namespace std;

int main()
{
    int n = 2019;
    cout << "implicit:    " << n << endl;
    cout << "decimal:     " << dec << n << endl;
    cout << "octal:       " << oct << n << endl;
    cout << "hexadecimal: " << hex << n << endl;
    cout << "Hello world!" << endl;
    return 0;
}
